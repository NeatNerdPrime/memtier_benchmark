#ifndef MEMTIER_BENCHMARK_CLUSTER_CLIENT_H
#define MEMTIER_BENCHMARK_CLUSTER_CLIENT_H

#include <set>
#include <queue>
#include <string>
#include <vector>
#include "client.h"

typedef std::queue<unsigned long long> key_index_pool;

struct staged_monitor_cmd
{
    arbitrary_command parsed_cmd; // already split (split_command_to_args done); format at drain
    size_t stats_index;
    struct timeval enqueue_time; // latency measured from selection, not drain
    size_t source_line;          // original monitor file line (1-based) for error messages
};

// forward decleration
class shard_connection;

// Topology grouping: one shard_group per primary, plus the list of replicas
// (zero-length when the primary has no replicas, or when CLUSTER SLOTS only
// returned the primary tuple). Per-thread state (replica_rr_cursor) — no
// atomics because each cluster_client lives on a single thread.
//
// id is the group's index in m_shard_groups. m_slot_to_shard_group[slot] points
// back to that index, so the primary serving slot s is
// m_shard_groups[m_slot_to_shard_group[s]].primary. Replica selection is
// owned by a future select_target_conn(); this struct is the data plumbing.
struct shard_group
{
    unsigned int id;
    shard_connection *primary;
    std::vector<shard_connection *> replicas;
    unsigned int replica_rr_cursor; // per-thread, not atomic
};

class cluster_client : public client
{
public:
    // Per-conn observability: dispatch dispatches Ops-from-Primary /
    // Ops-from-Replica counts per arbitrary command type. Implemented as a
    // small fixed-size table indexed by (arbitrary_command_index, role).
    // Used by run_stats::print_json via accessor below to populate the
    // per-command "Read Routing" object.
    struct read_routing_counters
    {
        unsigned long long ops_from_primary;
        unsigned long long ops_from_replica;
        read_routing_counters() : ops_from_primary(0), ops_from_replica(0) {}
    };

protected:
    std::vector<key_index_pool *> m_key_index_pools;
    std::vector<std::queue<staged_monitor_cmd> > m_staged_monitor_commands;
    // Per-slot map to the owning shard group. m_shard_groups[m_slot_to_shard_group[s]].primary
    // is the connection that owns slot s; replicas are reachable via the same group's
    // replicas vector. Replaces the old `unsigned int m_slot_to_shard[16384]` (which only
    // tracked primary conn_id by slot).
    std::vector<unsigned int> m_slot_to_shard_group;
    // One entry per primary discovered via CLUSTER SLOTS; index == shard_group::id.
    // Indices are stable within a topology refresh: handle_cluster_slots() rebuilds the
    // vector each call so groups stay in step with the rest of the rewritten map.
    std::vector<shard_group> m_shard_groups;
    // --transaction: shard connection that owns the in-flight rotation of
    // --command entries. -1 = no pin (rotation has not started or just ended).
    // The pin is established when the first command of a fresh rotation is
    // processed and cleared when m_arbitrary_command_rotation_seq advances
    // past m_txn_observed_rotation_seq (i.e. the previous rotation wrapped).
    int m_txn_pinned_conn_id;
    unsigned long long m_txn_observed_rotation_seq;
    // Key index pre-generated by the lookahead for the first keyed command of
    // the pinned rotation. Stored here (not in the shared pool) to avoid
    // corrupting the pool's (command_index, key_index) pair invariant.
    unsigned long long m_txn_staged_key_index;
    bool m_txn_has_staged_key;
    // Set when we emit the "pin connection lost mid-rotation" warning so we
    // don't spam it on every hold_pipeline() call during the outage.
    bool m_txn_pin_lost_warned;
    // Consecutive read-routing failures under strict rp_secondary with no live
    // replica. Incremented in get_key_for_conn each time select_target_conn
    // returns UINT_MAX for a read; reset on any successful routing. When the
    // counter reaches STRICT_NO_ROUTE_HOLD_THRESHOLD hold_pipeline returns true
    // (yields the event loop) until a replica connects and schedule_fill wakes
    // the connection. Prevents busy-spinning fill_pipeline in mixed SET/GET
    // workloads where writes succeed (primary is live) but every GET routing
    // attempt fails because no replica is available yet.
    unsigned int m_strict_no_route_attempts;
    // Monotonic timestamp (CLOCK_MONOTONIC seconds) of the last "all replicas
    // unreachable under read-only workload" warning emitted from
    // hold_pipeline. Rate-limited to once per 60s so a stalled benchmark
    // produces an operator-visible signal without log spam. 0 means never
    // emitted.
    long long m_last_no_replica_warning_ts;
    // Per-slot key index cache for cluster MGET. The slot→key table
    // (slot_keys) is shared across threads via m_config->mget_cache and is
    // read-only after the first thread builds it.  Only the per-slot
    // round-robin cursor (m_mget_slot_cursor) and the per-connection slot
    // list (m_mget_conn_slots) are per-thread.
    std::vector<size_t> m_mget_slot_cursor;                    // [slot] → per-thread round-robin cursor
    std::vector<std::vector<unsigned int> > m_mget_conn_slots; // [conn] → owned slot list
    std::vector<size_t> m_mget_conn_slot_cursor;               // [conn] → slot round-robin cursor
    void build_mget_slot_cache();

    // Per-arbitrary-command-index routing counters (sized to the number of
    // entries in --command at startup; entries beyond that are silently
    // dropped). The built-in counters split out GET (the only built-in read
    // class) and report under "Gets" in the JSON.
    std::vector<read_routing_counters> m_arbitrary_routing_counters;
    read_routing_counters m_get_routing_counters;

    virtual int connect(void);
    virtual void disconnect(void);

    // Primaries-only slot lookup. Resolves a hash slot through the
    // m_slot_to_shard_group indirection to the conn_id of the owning primary.
    // Behavior is identical to the prior `m_slot_to_shard[slot]` array — this
    // is the canonical primary-routing helper while replica selection lives
    // behind a future select_target_conn() (owned by the integration agent).
    // Returns UINT_MAX if the slot maps to an unpopulated group (shouldn't
    // happen post-bootstrap CLUSTER SLOTS, but defensive against torn state).
    unsigned int slot_primary_conn_id(unsigned int slot) const;

    // Read-routing dispatch. Resolves a hash slot to the conn_id of the
    // shard_connection that should serve a request under the configured
    // --read-preference. Writes (is_read == false) always go to the
    // primary. Reads route per mode:
    //
    //   rp_primary             -> primary
    //   rp_secondary           -> next live replica (RR); UINT_MAX if no live
    //                             replica (caller applies fallback policy)
    //   rp_secondary_preferred -> next live replica, else primary
    //   rp_nearest             -> lowest EWMA-latency among warm replicas+primary
    //
    // "Live" means connection_state==conn_connected AND cluster_slots_state==
    // setup_done (the READONLY ladder having lit setup_done is implicit in
    // setup_done for replicas because the readonly ladder is checked alongside).
    //
    // Returns UINT_MAX when the slot is unmapped (bootstrap window) or when
    // strict `rp_secondary` has no live replicas. Callers must handle the
    // UINT_MAX case explicitly (typically by treating as not_available and
    // letting the next tick retry, or applying --read-preference-fallback for
    // the strict-secondary case).
    unsigned int select_target_conn(unsigned int slot, bool is_read);

    // True if the request should be classified as a read for routing
    // purposes (see select_target_conn). Inspects request type plus
    // arbitrary_command::is_read_override and CommandSpec::is_read. Returns
    // false for WAIT, MULTI/EXEC, EVAL/EVALSHA (write-classified by default),
    // and any unknown command (safe default).
    bool classify_read(const request *req) const;

    // command_index -> is_read variant of classify_read, for the synchronous
    // routing path inside get_key_for_conn (where there is no request*
    // available yet). When arbitrary_commands is defined, command_index
    // addresses arbitrary_commands[]; otherwise it's SET_CMD_IDX / GET_CMD_IDX.
    bool is_read_command_index(unsigned int command_index) const;

    // Bump the per-command Ops-from-Primary / Ops-from-Replica counter.
    void record_read_routing(size_t arbitrary_index, bool from_replica);
    void record_builtin_read_routing(request_type rt, bool from_replica);

    shard_connection *create_shard_connection(abstract_protocol *abs_protocol);
    bool connect_shard_connection(shard_connection *sc, char *address, char *port);
    void handle_moved(unsigned int conn_id, struct timeval timestamp, request *request, protocol_response *response);
    void handle_ask(unsigned int conn_id, struct timeval timestamp, request *request, protocol_response *response);
    bool create_monitor_request_cluster(unsigned int command_index, struct timeval &timestamp, unsigned int conn_id);
    void process_staged_monitor_command(struct timeval timestamp, unsigned int conn_id);
    // Resend the request on the slot-owning connection (or same connection if
    // routing info isn't available yet). Returns true if ownership of `req`
    // transferred to a retry queue.
    bool retry_after_redirect(unsigned int conn_id, request *req);
    // Terminal accounting for a MOVED/ASK request whose retry was refused
    // (e.g. max_retries exhausted, retry queue full, no captured bytes). Logs
    // to --failed-keys-file if configured and increments the error counter,
    // so the request doesn't silently disappear from the stats.
    void finalize_dropped_redirect(struct timeval timestamp, request *req, protocol_response *response);
    // Release the transaction pin and return any staged key to the pool so the
    // key index is not silently burned when a mid-rotation MOVED or disconnect
    // forces the rotation to restart.
    void txn_release_pin();

public:
    cluster_client(client_group *group);
    virtual ~cluster_client();

    virtual get_key_response get_key_for_conn(unsigned int command_index, unsigned int conn_id,
                                              unsigned long long *key_index);
    virtual bool create_arbitrary_request(unsigned int command_index, struct timeval &timestamp, unsigned int conn_id);
    virtual bool create_mget_request(struct timeval &timestamp, unsigned int conn_id);
    // Overriden so we can bump the per-command read-routing counter after
    // the base class actually sends. The base implementation handles all
    // the heavy lifting (key generation, routing-target sharding via
    // get_key_for_conn); we only annotate the routing outcome.
    virtual bool create_get_request(struct timeval &timestamp, unsigned int conn_id);

    // Snapshot accessors for the per-command read-routing counters. Read-only
    // and intended for the merge thread (post-join). See JSON_handler /
    // run_stats::print_json for the consumer.
    const std::vector<read_routing_counters> &get_arbitrary_routing_counters() const
    {
        return m_arbitrary_routing_counters;
    }
    const read_routing_counters &get_get_routing_counters() const { return m_get_routing_counters; }

    // client manager api's
    virtual bool handle_cluster_slots(protocol_response *r);
    virtual void create_request(struct timeval timestamp, unsigned int conn_id);
    virtual bool hold_pipeline(unsigned int conn_id);
    virtual void handle_response(unsigned int conn_id, struct timeval timestamp, request *request,
                                 protocol_response *response);
};


#endif // MEMTIER_BENCHMARK_CLUSTER_CLIENT_H
